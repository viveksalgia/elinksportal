package com.enterprise.links.beans;

public class ConfigBean {
	private String Name;
	private String Value;
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getValue() {
		return Value;
	}
	public void setValue(String value) {
		Value = value;
	}

}
